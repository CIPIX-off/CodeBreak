def CLEAR():
    os.system('cls' if os.name == 'nt' else 'clear')

def display_animation():
    for frame in frames:
        CLEAR()
        print(colorDiscord + frame + reset)
        time.sleep(temps)

def discordAnim(fichier):
    display_animation()
    print(f'{TIME()} {WAIT} loading... {reset}')
    if fichier == 'null':
        mainMenu()
    else:
        try:
            startProgram(fichier)
        except:
            mainMenu()
